package com.flamey;

public class Dimensions
{
    private int width;
    private int height;
    private int depthl;

    public Dimensions(int width, int height, int depthl)
    {
        this.width = width;
        this.height = height;
        this.depthl = depthl;
    }

    public int getWidth()
    {
        return width;
    }

    public int getHeight()
    {
        return height;
    }

    public int getDepthl()
    {
        return depthl;
    }
}
